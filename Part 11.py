#operasi assignment


a = 5
print('Nilai a =', a)

a += 1 # artinya adalah a = a + 1
print('Nilai a += 1, nilai a menjadi = ', a)

a -= 2 # artinya adalah a = a - 2
print('Nilai a -= 2, nilai a menjadi = ', a)

a *= 5 # artinya adalah a = a * 2
print('Nilai a *= 2, nilai a menjadi = ', a)

a /= 2 # artinya adalah a = a / 2
print('Nilai a /= 2, nilai a menjadi = ', a)

